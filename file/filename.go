package file

import (
	"path/filepath"
	"strings"
)

func BetterFilename(filename string) string {
	filename = strings.TrimSpace(filename)

	switch filename {
	case "", ".", "..":
		return filename
	default:
		return filepath.Clean(filename)
	}
}
